    private void bloginActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_bloginActionPerformed
        String username = jtuser.getText().trim();
        String password = new String(jpassword.getPassword()).trim();

        if (username.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your username", "Input Error",
                    JOptionPane.WARNING_MESSAGE);
            jtuser.requestFocus();
            return;
        }

        if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your password", "Input Error",
                    JOptionPane.WARNING_MESSAGE);
            jpassword.requestFocus();
            return;
        }

        try {
            String filename = "Admin.txt";
            UserDatabase userDB = new UserDatabase(filename);
            userDB.readFromFile();

            // Debug: Check what's in the database
            System.out.println("Total users in database: " + userDB.returnAllRecords().size());
            for (AdminUser user : userDB.returnAllRecords()) {
                System.out.println("User: " + user.getSearchKey() + ", Pass: " + user.getPassword());
            }

            if (!userDB.containsName(username)) {
                JOptionPane.showMessageDialog(this, "Invalid username", "Login Failed", JOptionPane.ERROR_MESSAGE);
                jtuser.selectAll();
                jtuser.requestFocus();
                return;
            }

            // Get the user and check password
            AdminUser user = userDB.getRecord(username);
            if (user != null && user.getPassword().equals(password)) {
                // Successful login
                JOptionPane.showMessageDialog(this, "Login successful! Welcome " + username, "Success",
                        JOptionPane.INFORMATION_MESSAGE);

                // Navigate to HomeFrame
                HomeFrame homeFrame = new HomeFrame();
                homeFrame.setVisible(true);
                this.dispose(); // Close login window
            } else {
                JOptionPane.showMessageDialog(this, "Invalid password", "Login Failed", JOptionPane.ERROR_MESSAGE);
                jpassword.selectAll();
                jpassword.requestFocus();
            }

        } catch (Exception e) {
            logger.severe("Login error: " + e.getMessage());
            e.printStackTrace();

            // Try to fix the admin file with correct format
            fixAdminFile();

            JOptionPane.showMessageDialog(this,
                    "System error detected. Admin file has been reset.\n\n" +
                            "Please try again with:\n" +
                            "Username: admin\n" +
                            "Password: admin123",
                    "System Reset",
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }// GEN-LAST:event_bloginActionPerformed

    private void jcshow_passwordActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jcshow_passwordActionPerformed
        if (jcshow_password.isSelected()) {
            jpassword.setEchoChar((char) 0); // Show password
        } else {
            jpassword.setEchoChar('*'); // Hide password
        }
    }// GEN-LAST:event_jcshow_passwordActionPerformed

    /**
     * Fix corrupted admin file by recreating it with COMMA format
     */
    private void fixAdminFile() {
        try {
            File adminFile = new File("Admin.txt");
            if (adminFile.exists()) {
                adminFile.delete();
            }
            createDefaultAdminFile();
            logger.info("Admin file has been reset with comma format");
        } catch (Exception e) {
            logger.severe("Error fixing admin file: " + e.getMessage());
        }
    }

