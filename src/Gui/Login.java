    /**
     * Create default admin file with COMMA format (not space)
     */
    private void createDefaultAdminFile() {
        File adminFile = new File("Admin.txt");
        if (!adminFile.exists()) {
            try {
                FileWriter writer = new FileWriter(adminFile);
                writer.write("admin,admin123\n");
                writer.write("user,password\n");
                writer.close();
                logger.info("Default admin file created successfully with comma format");
            } catch (IOException e) {
                logger.severe("Error creating admin file: " + e.getMessage());
            }
        }
    }

